package Dogs;

public class CompareLoops {
	

	public static void whileL() {
		int count = 2;
	
	while (count<2) {
		count ++;
		System.out.println(count);
	}
	}

	public static void doWhile() {
		int count = 0;
		System.out.println("do while");
		
		do { 
			count ++;
			System.out.println(count);
		}
		while (count<2);
		
	}
	}
