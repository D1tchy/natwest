package switchCases;

public class Months {
	public static String MonthsOfYear(String months) {
		switch (months) {
		case "January":
			System.out.println("January is cold");
			break;
		case "February":
			System.out.println("February is colder");
			break;
		case "March":
			System.out.println("Get them seeds in");
			break;
		case "April":
			System.out.println("April Showers");
			break;
		case "May":
			System.out.println("Out come the Roses");
			break;
		case "June":
			System.out.println("Peonies to the party");
			break;
		case "July":
			System.out.println("hot hot hot");
			break;
		case "August":
			System.out.println("Chilli time");
			break;
		case "September":
			System.out.println("its my Birthday");
			break;
		case "October":
			System.out.println("empty nest");
			break;
		case "November":
			System.out.println("pumpkin time");
			break;
		case "December":
			System.out.println("Happy Christmas sweet cheeks");
			break;
		default:
			System.out.println("invalid option");
			break;

		}
		return null;
	}

}

		
		
	
	